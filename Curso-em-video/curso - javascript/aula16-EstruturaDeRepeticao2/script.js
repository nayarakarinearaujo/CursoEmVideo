
//!Estrutura de repetição parte 2

/*
* Com variável de controle: 
* Estrutura do for:
* for(inicio; teste; incre) { 
* }
*/

//Exemplo com while
var c = 1;
while (c <= 10) {
    console.log(`Passo ${c}`)
    c++
}

//Exemplo com for
for (var c = 1; c <= 10; c++) {
    console.log(`Passo ${c}`)
}

//* Ambos tem a mesma funcionalidade